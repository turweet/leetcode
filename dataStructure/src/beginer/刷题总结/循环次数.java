package beginer.刷题总结;

public class 循环次数 {

    public static void main(String[] args) {
        int x = 5;
        for (int i = 0; i < x; i++) { //循环5次，可用于遍历数组
        }

        for (int i = 1; i <= x + 1; i++) { //循环6次，比x值多1
        }

        for (int i = 0; i <= x; i++) { // 循环6次，比x值多1
        }
    }
}
